package practice.q02;

public class Employee {
	//フィールド変数 name, age を宣言
	private String name;
	private int age;

	//コンストラクタ
	public Employee() {
		super();
	}

	//name 
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	//age
	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
